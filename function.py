# A parameter is a piece of information given to a function

def sayhi():
    print("Hello, Rhoda!")

sayhi()

# parameters

def sayhello(name, age):
    print("Hello " + name + ", you are " + age + " years old!")

sayhello("Adrian", "56")
sayhello("Ethan", "36")
sayhello("Rhoda", "16")
